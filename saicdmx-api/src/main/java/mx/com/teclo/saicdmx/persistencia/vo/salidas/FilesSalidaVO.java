package mx.com.teclo.saicdmx.persistencia.vo.salidas;


import java.sql.Blob;
import java.util.List;

public class FilesSalidaVO {

	private String filetype;
	private String filename;
	private String filesize;
	private String base64;
	private List<Blob> fileBase64toBlob;
	 
	public String getFiletype() {
		return filetype;
	}
	public void setFiletype(String filetype) {
		this.filetype = filetype;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public String getFilesize() {
		return filesize;
	}
	public void setFilesize(String filesize) {
		this.filesize = filesize;
	}
	public String getBase64() {
		return base64;
	}
	public void setBase64(String base64) {
		this.base64 = base64;
	}
	public List<Blob> getFileBase64toBlob() {
		return fileBase64toBlob;
	}
	public void setFileBase64toBlob(List<Blob> fileBase64toBlob) {
		this.fileBase64toBlob = fileBase64toBlob;
	}

	
}
