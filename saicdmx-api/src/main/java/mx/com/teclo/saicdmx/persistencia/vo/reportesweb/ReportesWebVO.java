package mx.com.teclo.saicdmx.persistencia.vo.reportesweb;

public class ReportesWebVO {
	private Long reporteId;
	private String nombreReporte;
	
	/**
	 * @return the reporteId
	 */
	public Long getReporteId() {
		return reporteId;
	}
	/**
	 * @param reporteId the reporteId to set
	 */
	public void setReporteId(Long reporteId) {
		this.reporteId = reporteId;
	}
	/**
	 * @return the nombreReporte
	 */
	public String getNombreReporte() {
		return nombreReporte;
	}
	/**
	 * @param nombreReporte the nombreReporte to set
	 */
	public void setNombreReporte(String nombreReporte) {
		this.nombreReporte = nombreReporte;
	}
}
