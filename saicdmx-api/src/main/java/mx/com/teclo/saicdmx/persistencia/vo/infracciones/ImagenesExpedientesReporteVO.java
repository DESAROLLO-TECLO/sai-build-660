	public void setFiletype(String filetype) {
		this.filetype = filetype;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public String getFilesize() {
		return filesize;
	}
	public void setFilesize(String filesize) {
		this.filesize = filesize;
	}
	public String getBase64() {
		return base64;
	}
	public void setBase64(String base64) {
		this.base64 = base64;
	}
	public ByteArrayInputStream getFileBase64toBlob() {
		return fileBase64toBlob;
	}
	public void setFileBase64toBlob(ByteArrayInputStream fileBase64toBlob) {
		this.fileBase64toBlob = fileBase64toBlob;
	}
	public String getFileextencion() {
		return fileextencion;
	}
	public void setFileextencion(String fileextencion) {
		this.fileextencion = fileextencion;
	}

	public Boolean getFileEnBase() {
		return fileEnBase;
	}
	public void setFileEnBase(Boolean fileEnBase) {
		this.fileEnBase = fileEnBase;
	}
	public String getFileNombreCatDocumento() {
		return fileNombreCatDocumento;
	}
	public void setFileNombreCatDocumento(String fileNombreCatDocumento) {
		this.fileNombreCatDocumento = fileNombreCatDocumento;
	}
