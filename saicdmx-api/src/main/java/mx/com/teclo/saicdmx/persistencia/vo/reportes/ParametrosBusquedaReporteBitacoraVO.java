package mx.com.teclo.saicdmx.persistencia.vo.reportes;

import java.util.List;

public class ParametrosBusquedaReporteBitacoraVO {
	
	private Long conponenteId;
	private List<String> conceptosId;
	private String fechaInicio;
	private String fehaFin;
	private List<TcaBitacoraCambiosVO> listaBitacora;
	
	
	private String componente;
	private String concepto;
	/**
	 * @return the conponenteId
	 */
	public Long getConponenteId() {
		return conponenteId;
	}
	/**
	 * @param conponenteId the conponenteId to set
	 */
	public void setConponenteId(Long conponenteId) {
		this.conponenteId = conponenteId;
	}
	/**
	 * @return the conceptosId
	 */
	public List<String> getConceptosId() {
		return conceptosId;
	}
	/**
	 * @param conceptosId the conceptosId to set
	 */
	public void setConceptosId(List<String> conceptosId) {
		this.conceptosId = conceptosId;
	}
	/**
	 * @return the fechaInicio
	 */
	public String getFechaInicio() {
		return fechaInicio;
	}
	/**
	 * @param fechaInicio the fechaInicio to set
	 */
	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	/**
	 * @return the fehaFin
	 */
	public String getFehaFin() {
		return fehaFin;
	}
	/**
	 * @param fehaFin the fehaFin to set
	 */
	public void setFehaFin(String fehaFin) {
		this.fehaFin = fehaFin;
	}
	/**
	 * @return the listaBitacora
	 */
	public List<TcaBitacoraCambiosVO> getListaBitacora() {
		return listaBitacora;
	}
	/**
	 * @param listaBitacora the listaBitacora to set
	 */
	public void setListaBitacora(List<TcaBitacoraCambiosVO> listaBitacora) {
		this.listaBitacora = listaBitacora;
	}
	/**
	 * @return the componente
	 */
	public String getComponente() {
		return componente;
	}
	/**
	 * @param componente the componente to set
	 */
	public void setComponente(String componente) {
		this.componente = componente;
	}
	/**
	 * @return the concepto
	 */
	public String getConcepto() {
		return concepto;
	}
	/**
	 * @param concepto the concepto to set
	 */
	public void setConcepto(String concepto) {
		this.concepto = concepto;
	}
	
	
}
