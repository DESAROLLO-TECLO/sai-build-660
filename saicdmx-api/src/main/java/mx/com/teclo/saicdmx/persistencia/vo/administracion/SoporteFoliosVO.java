package mx.com.teclo.saicdmx.persistencia.vo.administracion;

public class SoporteFoliosVO {

	private Integer cantidad;
	private String folioClave;
	
	
	/**
	 * @return the cantidad
	 */
	public Integer getCantidad() {
		return cantidad;
	}
	/**
	 * @param cantidad the cantidad to set
	 */
	public void setCantidad(Integer cantidad) {
		this.cantidad = cantidad;
	}
	/**
	 * @return the folioClave
	 */
	public String getFolioClave() {
		return folioClave;
	}
	/**
	 * @param folioClave the folioClave to set
	 */
	public void setFolioClave(String folioClave) {
		this.folioClave = folioClave;
	}
	
}
