package mx.com.teclo.saicdmx.persistencia.vo.reportes;

public class InfraccionesporTipoVehiculoVO {
	private String fecha;
	private String folio;
	private String fecha_Hora;
	private String articulo;
	private String fraccion;
	private String tipo_Vehiculo;
	private String delegacion;
	
	
	/**
	 * @return the fecha
	 */
	public String getFecha() {
		return fecha;
	}
	/**
	 * @param fecha the fecha to set
	 */
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	/**
	 * @return the folio
	 */
	public String getFolio() {
		return folio;
	}
	/**
	 * @param folio the folio to set
	 */
	public void setFolio(String folio) {
		this.folio = folio;
	}
	/**
	 * @return the fecha_Hora
	 */
	public String getFecha_Hora() {
		return fecha_Hora;
	}
	/**
	 * @param fecha_Hora the fecha_Hora to set
	 */
	public void setFecha_Hora(String fecha_Hora) {
		this.fecha_Hora = fecha_Hora;
	}
	/**
	 * @return the articulo
	 */
	public String getArticulo() {
		return articulo;
	}
	/**
	 * @param articulo the articulo to set
	 */
	public void setArticulo(String articulo) {
		this.articulo = articulo;
	}
	/**
	 * @return the fraccion
	 */
	public String getFraccion() {
		return fraccion;
	}
	/**
	 * @param fraccion the fraccion to set
	 */
	public void setFraccion(String fraccion) {
		this.fraccion = fraccion;
	}
	/**
	 * @return the tipo_Vehiculo
	 */
	public String getTipo_Vehiculo() {
		return tipo_Vehiculo;
	}
	/**
	 * @param tipo_Vehiculo the tipo_Vehiculo to set
	 */
	public void setTipo_Vehiculo(String tipo_Vehiculo) {
		this.tipo_Vehiculo = tipo_Vehiculo;
	}
	/**
	 * @return the delegacion
	 */
	public String getDelegacion() {
		return delegacion;
	}
	/**
	 * @param delegacion the delegacion to set
	 */
	public void setDelegacion(String delegacion) {
		this.delegacion = delegacion;
	}
	
	

}
