package mx.com.teclo.saicdmx.persistencia.vo.caja;

public class VFoliosCajaVO {

	private String cajaId;
	private String cajaCod;
	private String ultimoFolioSistema;
	private String ultimoFolioCaja;
	private String tipoFolio;
	
	public String getCajaId() {
		return cajaId;
	}
	public void setCajaId(String cajaId) {
		this.cajaId = cajaId;
	}
	public String getCajaCod() {
		return cajaCod;
	}
	public void setCajaCod(String cajaCod) {
		this.cajaCod = cajaCod;
	}
	public String getUltimoFolioSistema() {
		return ultimoFolioSistema;
	}
	public void setUltimoFolioSistema(String ultimoFolioSistema) {
		this.ultimoFolioSistema = ultimoFolioSistema;
	}
	public String getUltimoFolioCaja() {
		return ultimoFolioCaja;
	}
	public void setUltimoFolioCaja(String ultimoFolioCaja) {
		this.ultimoFolioCaja = ultimoFolioCaja;
	}
	public String getTipoFolio() {
		return tipoFolio;
	}
	public void setTipoFolio(String tipoFolio) {
		this.tipoFolio = tipoFolio;
	}
	
}
